package com.tcl.entity;

import java.util.List;

public class Result {
	private int total;

	private String updateTime;

	private List<HotPlayInfo> hotProvince;

	public int getTotal() {
		return total;
	}

	public void setTotal(int total) {
		this.total = total;
	}

	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}

	public String getUpdateTime() {
		return this.updateTime;
	}

	public List<HotPlayInfo> getHotProvince() {
		return hotProvince;
	}

	public void setHotProvince(List<HotPlayInfo> hotProvince) {
		this.hotProvince = hotProvince;
	}

}